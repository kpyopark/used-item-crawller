package structure;

public class CategoryNode {
	
	String categoryURL;
	String title;
	public String getCategoryURL() {
		return categoryURL;
	}
	public void setCategoryURL(String categoryURL) {
		this.categoryURL = categoryURL;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
}